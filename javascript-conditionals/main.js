function recommendMovie(genre) {
  switch (genre.toLowerCase()) {
    case 'action':
      return '57 Seconds';
    case 'comedy':
      return 'Old Dads';
    case 'horror':
      return 'Hereditary';
    case 'drama':
      return 'The Son';
    case 'musical':
      return 'Tick, Tick... Boom!';
    case 'sci-fi':
      return 'Blade Runner 2049';
  }
}
